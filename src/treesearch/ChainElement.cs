using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibHTreeProcessing.src.treesearch
{

	public class ChainElement
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		public readonly AbstractTreeNodeVisitor Visitor;
		public readonly AbstractTreeNodeMatcher Matcher;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public ChainElement(AbstractTreeNodeVisitor visitor,
			AbstractTreeNodeMatcher matcher)
		{
			if (matcher == null) throw new Exception("matcher == null!");

			this.Visitor = visitor;
			this.Matcher = matcher;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public override string ToString()
		{
			StringBuilder sb = new StringBuilder();
			ToString(sb);
			sb.ToString();
			return sb.ToString();
		}

		public void ToString(StringBuilder sb)
		{
			sb.Append("/" + Visitor.ToString());
			sb.Append(":");
			sb.Append(Matcher.ToString());
		}

	}

}
