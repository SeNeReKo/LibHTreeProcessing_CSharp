using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibHTreeProcessing.src.simplexml
{

	public class ExpressionCompilerException : Exception
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public ExpressionCompilerException(string message, string line, int position)
			: base(message)
		{
			this.Line = line;
			this.CharacterPosition = position;
		}

		public ExpressionCompilerException(string line, int position)
			: base("Syntax error!")
		{
			this.Line = line;
			this.CharacterPosition = position;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		public string Line
		{
			get;
			private set;
		}

		public int CharacterPosition
		{
			get;
			private set;
		}

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

	}

}
