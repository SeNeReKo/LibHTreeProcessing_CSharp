using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;


namespace LibHTreeProcessing.src.stringmatching
{

	public class StringMatcherContains : AbstractStringMatcher
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		string textFragment;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public StringMatcherContains(string textFragment)
		{
			this.textFragment = textFragment;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public override bool Match(string text)
		{
			return text.IndexOf(textFragment) >= 0;
		}

		public override string ToString()
		{
			return "contains \"" + textFragment + "\"";
		}

	}

}
